    private void BubActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BubActionPerformed
       String in = JOptionPane.showInputDialog(this, "Buscar:");
        for (int i = 0; i < lis.size(); i++) {
            if(lis.get(i).nombre.equalsIgnoreCase(in)){
                JOptionPane.showMessageDialog(Bub, "Encontrado", "Buscar Producto", 2);
                t1.setText(lis.get(i).getNombre());
                t2.setText(String.valueOf(lis.get(i).getPrecio()));
                t3.setText(lis.get(i).getMarca());
                t4.setText(lis.get(i).getColor());
                t5.setText(lis.get(i).getTalla());
                
                return;
            }
        }
        JOptionPane.showMessageDialog(Bub, "No Encontrado!!!", "Buscar Producto", 2);
    }//GEN-LAST:event_BubActionPerformed

