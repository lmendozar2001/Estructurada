    private void aggbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aggbActionPerformed
       String nom = txtnombre.getText();
       float pr = Float.parseFloat(txtprecio.getText());
       String mar = txtmarca.getText();
       String color = txtcolor.getText();
       String talla = txttalla.getText();
       
       lis.add(new Productos(pr,talla,color,mar,nom));
       mod.setRowCount(0);
        for (int i = 0; i < lis.size(); i++) {
            Object[] ob = {lis.get(i).getNombre(),lis.get(i).getPrecio(),lis.get(i).getMarca(),lis.get(i).getColor(),lis.get(i).getTalla()};
            mod.addRow(ob);
        }
        clearField();
    }//GEN-LAST:event_aggbActionPerformed
    
